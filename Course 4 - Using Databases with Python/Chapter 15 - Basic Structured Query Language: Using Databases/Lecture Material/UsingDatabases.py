'Two Roles in Large Projects'
#Application Developer - Builds the logic for the application, the look and feel of the application - monitors the application for problems
#*Database Administrator - Monitors and adjusts the databases as the program runs in production
#Often both participate in the building of the "Data model"

'Database Administrator (dba)'
#A database administrator (DBA) is a person responsible for the design, implementation, maintenance, and repair
#of an organization's database. The role includes the development and deisgn of database strategies, monitoring and improving database performance
#and capacity, and planning for future expansion requirements. They may also plan, coordinate, and implement security measures to safeguard the database.

'Database Model'
#A database model or database schema is the structure or format of a database, described in a formal language supported by the database management
#sytem. In other words, a "database model" is the application of a data model when used in conjunction with a database management system.

